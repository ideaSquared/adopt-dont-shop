#!/usr/bin/env node
/**
 * Per-lib.* "who consumes me" doc generator (ADS-957).
 *
 * A contributor about to change a `lib.*` package has no easy way to know
 * who depends on it. This script parses every workspace `package.json`
 * (apps/*, packages/*, services/*) for `@adopt-dont-shop/*` dependencies,
 * builds a reverse map for each `lib.*` package, and writes:
 *
 *   - `docs/libraries/<lib>-consumers.md` — the consumer list itself.
 *   - A `## Consumers` section in each `lib.*` package's README, linking to
 *     that file (idempotent — replaces a previously generated section
 *     rather than duplicating it).
 *   - The "Library consumer lists" block in `docs/README.md` so every
 *     generated file stays linked from the docs index (required by
 *     `scripts/check-docs-index.mjs`).
 *
 * Run via `node scripts/generate-dependency-docs.mjs` or
 * `pnpm generate:dependency-docs`. Wired into the `dependency-graph.yml`
 * nightly/on-push workflow, which commits the regenerated files.
 */
import { existsSync, readFileSync, readdirSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const FAMILIES = ['apps', 'packages', 'services'];
const DOCS_LIBRARIES_DIR = join(ROOT, 'docs', 'libraries');
const DOCS_README = join(ROOT, 'docs', 'README.md');
const SCOPE = '@adopt-dont-shop/';

const README_SECTION_START =
  '<!-- CONSUMERS:START (auto-generated by scripts/generate-dependency-docs.mjs — do not edit by hand) -->';
const README_SECTION_END = '<!-- CONSUMERS:END -->';

const DOCS_INDEX_SECTION_START =
  '<!-- LIB-CONSUMER-LISTS:START (auto-generated by scripts/generate-dependency-docs.mjs — do not edit by hand) -->';
const DOCS_INDEX_SECTION_END = '<!-- LIB-CONSUMER-LISTS:END -->';

// --- Discovery -------------------------------------------------------------

/**
 * @returns {{ name: string, dir: string, deps: string[] }[]}
 */
export function readWorkspacePackages(root = ROOT, families = FAMILIES) {
  const packages = [];
  for (const family of families) {
    let entries;
    try {
      entries = readdirSync(join(root, family), { withFileTypes: true });
    } catch {
      continue;
    }
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;
      const pkgPath = join(root, family, entry.name, 'package.json');
      try {
        const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
        if (!pkg.name) continue;
        const deps = { ...pkg.dependencies, ...pkg.devDependencies };
        const workspaceDeps = Object.keys(deps).filter(d => d.startsWith(SCOPE));
        packages.push({ name: pkg.name, dir: `${family}/${entry.name}`, deps: workspaceDeps });
      } catch {
        // No package.json — skip.
      }
    }
  }
  return packages;
}

// --- Pure transforms ---------------------------------------------------------

export function libShortName(scopedName) {
  return scopedName.replace(SCOPE, '');
}

export function consumerDocFileName(scopedName) {
  return `${libShortName(scopedName)}-consumers.md`;
}

/**
 * Build a reverse map: every `lib.*` package name -> its sorted consumers.
 *
 * @param {{ name: string, dir: string, deps: string[] }[]} packages
 * @returns {Map<string, { name: string, dir: string }[]>}
 */
export function buildConsumerMap(packages) {
  const libPackages = packages.filter(p => p.name.startsWith(`${SCOPE}lib.`));
  const map = new Map();
  for (const lib of libPackages) {
    const consumers = packages
      .filter(p => p.name !== lib.name && p.deps.includes(lib.name))
      .map(p => ({ name: p.name, dir: p.dir }))
      .sort((a, b) => a.name.localeCompare(b.name));
    map.set(lib.name, consumers);
  }
  return map;
}

export function formatConsumerDoc(scopedName, consumers) {
  const lines = [
    `# Consumers of \`${scopedName}\``,
    '',
    '_Auto-generated by `scripts/generate-dependency-docs.mjs` — do not edit by hand._',
    '',
  ];
  if (consumers.length === 0) {
    lines.push('No workspace package currently depends on this library.');
  } else {
    lines.push(`${consumers.length} workspace package(s) depend on \`${scopedName}\`:`);
    lines.push('');
    for (const c of consumers) lines.push(`- \`${c.name}\` (\`${c.dir}\`)`);
  }
  lines.push('');
  return lines.join('\n');
}

export function readmeConsumersSection(scopedName, consumers) {
  const fileName = consumerDocFileName(scopedName);
  return [
    README_SECTION_START,
    '## Consumers',
    '',
    `${consumers.length} workspace package(s) depend on this library. See [${fileName}](../../docs/libraries/${fileName}) ` +
      'for the auto-generated list — check it before making a breaking change.',
    README_SECTION_END,
  ].join('\n');
}

export function docsIndexLibraryListSection(map) {
  const lines = [DOCS_INDEX_SECTION_START, '### Library consumer lists', ''];
  const sorted = [...map.entries()].sort((a, b) => a[0].localeCompare(b[0]));
  for (const [scopedName, consumers] of sorted) {
    const fileName = consumerDocFileName(scopedName);
    lines.push(
      `- [${libShortName(scopedName)} consumers](./libraries/${fileName}) — ${consumers.length} consumer(s)`
    );
  }
  lines.push(DOCS_INDEX_SECTION_END);
  return lines.join('\n');
}

/**
 * Idempotently replace a previously-generated marked block, or append one
 * if it isn't present yet.
 */
export function upsertMarkedSection(content, startMarker, endMarker, replacement) {
  const escaped = s => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const re = new RegExp(`${escaped(startMarker)}[\\s\\S]*?${escaped(endMarker)}`);
  if (re.test(content)) {
    return content.replace(re, replacement);
  }
  return `${content.trimEnd()}\n\n${replacement}\n`;
}

// --- Orchestration (impure) --------------------------------------------------

function main() {
  const packages = readWorkspacePackages();
  const map = buildConsumerMap(packages);

  for (const [scopedName, consumers] of map) {
    const fileName = consumerDocFileName(scopedName);
    writeFileSync(join(DOCS_LIBRARIES_DIR, fileName), formatConsumerDoc(scopedName, consumers));

    const readmePath = join(ROOT, 'packages', libShortName(scopedName), 'README.md');
    if (!existsSync(readmePath)) continue;
    const content = readFileSync(readmePath, 'utf8');
    const updated = upsertMarkedSection(
      content,
      README_SECTION_START,
      README_SECTION_END,
      readmeConsumersSection(scopedName, consumers)
    );
    if (updated !== content) writeFileSync(readmePath, updated);
  }

  const indexContent = readFileSync(DOCS_README, 'utf8');
  const updatedIndex = upsertMarkedSection(
    indexContent,
    DOCS_INDEX_SECTION_START,
    DOCS_INDEX_SECTION_END,
    docsIndexLibraryListSection(map)
  );
  if (updatedIndex !== indexContent) writeFileSync(DOCS_README, updatedIndex);

  console.log(`Regenerated consumer docs for ${map.size} lib.* package(s).`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main();
}
